class Solution {
  public:
    Node* sortedMerge(Node* list1, Node* list2) {
        // code here
        Node dummy(0);
        Node* node = &dummy;

        while (list1 && list2) {
            if (list1->data < list2->data) {
                node->next = list1;
                list1 = list1->next;
            } else {
                node->next = list2;
                list2 = list2->next;
            }
            node = node->next;
        }

        if (list1) {
            node->next = list1;
        } else {
            node->next = list2;
        }

        return dummy.next;

    }
};